fun main() {
    //WHILE
    println("******WHILE*********")
    var i = 1

    while (i <= 10) {
        println("While  $i")
        i+=2;
    }
    //DO WHILE
    //RUN THE BLOCK AND CHECK THE CONDITION AFTER
    println("********DO WHILE*********")
    i = 1
    do {
        println("DO WHILE $i")
        i+=2;

    } while (i<=10)


}