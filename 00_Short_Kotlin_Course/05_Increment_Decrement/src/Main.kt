fun main() {
    //Increment decrement in kotlin looks like the one in Java
    var count = 1
    print("Count : $count \n")
    count += 1
    print("Count : $count \n")
    count += 3
    print("Count : $count \n")
    count -= 2
    print("Count : $count \n")

    //tricky output
    var i = 5;
    println(i++);
    print(i--);

}